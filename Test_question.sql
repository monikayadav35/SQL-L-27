-- 6. Display customer level using CASE based on total
-- spending.
-- Rules:
-- ● = 3000 → VIP

-- ● 1500 to 2999 → Premium
-- ● below 1500 → Regular

select c.customer_name,sum(o.order_amount),
case 
     when sum(o.order_amount)>=3000 then 'vip'
     when sum(o.order_amount) between 1500 and 2999 then 'premium'
     else 'regular'
end as customer_level
from customers as c join orders as o on c.customer_id=o.customer_id
group by c.customer_name 
having sum(o.order_amount); 

